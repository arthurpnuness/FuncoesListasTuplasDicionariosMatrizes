'''Dada a lista cores = ["vermelho", "verde", "azul", "amarelo"], imprima o segundo e o último elemento.'''

# Cria uma lista chamada "cores" com os elementos: 'vermelho', 'verde', 'azul', e 'amarelo'
cores = ["vermelho", "verde", "azul", "amarelo"]

# Atribui o segundo elemento da lista "cores" (índice 1) à variável cor1
cor1 = cores[1]  # 'verde'

# Atribui o quarto elemento da lista "cores" (índice 3) à variável cor2
cor2 = cores[3]  # 'amarelo'

# Imprime os valores das variáveis cor1 e cor2, separados por um espaço
print(cor1, cor2)  # Exibe: 'verde amarelo'


